package ru.innopolis.demo.models;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.Collections;

public class UserProfileDetails implements UserDetails {

    private long id;

    private String email;

    private String password;

    private Collection<? extends GrantedAuthority> roles;

    public UserProfileDetails(UserProfile userProfile) {
        id = userProfile.getId();
        email = userProfile.getEmail();
        password = userProfile.getPassword();
        roles = Collections.singletonList(new SimpleGrantedAuthority(userProfile.getRole().name()));
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return roles;
    }

    @Override
    public String getPassword() {
        return password;
    }

    public void setUsername(String email){
        this.email = email;
    }

    @Override
    public String getUsername() {
        return email;
    }

    public Long getId() {
        return id;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}